class RandomizedSet {
private:
    vector<int> v;
    unordered_map<int, int> m;

public:
    /** Initialize your data structure here. */
    RandomizedSet() {
        
    }
    
    /** Inserts a value to the set. Returns true if the set did not already contain the specified element. */
    bool insert(int val) {
        if(m.find(val) != m.end())
            return false;
        m[val] = v.size();
        v.push_back(val);
        return true;
    }
    
    /** Removes a value from the set. Returns true if the set contained the specified element. */
    bool remove(int val) {
        if(m.find(val) == m.end())
            return false;

        int lastVal = v.back();
        int lastIdx = v.size()-1;
        int oldIdx = m[val];

        // Modify hash map
        m[lastVal] = oldIdx;

        // Remove from vector
        v[oldIdx] = lastVal;
        v.pop_back();

        m.erase(val);

        return true;
    }
    
    /** Get a random element from the set. */
    int getRandom() {
        return v[rand() % v.size()];
    }
};

